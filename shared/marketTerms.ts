/**
 * Builds a sorted list of terms ready for replacement. Longer terms are placed
 * first so that multi-word terms (e.g. "iron condor") are matched before
 * shorter substrings (e.g. "condor").
 */
export function buildProtectedTerms(): string[] {
  const terms = new Set([...MARKET_TICKERS, ...MARKET_TERMS]);
  return Array.from(terms)
    .map(term => term.trim())
    .filter(term => term.length > 0)
    .sort((a, b) => b.length - a.length);
}
